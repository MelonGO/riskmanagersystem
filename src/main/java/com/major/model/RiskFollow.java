package com.major.model;

public class RiskFollow {
	private int id;
	private int risk_id;
	private int risk_classId;
	private String create_time;
	private String description;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRisk_id() {
		return risk_id;
	}
	public void setRisk_id(int risk_id) {
		this.risk_id = risk_id;
	}
	public int getRisk_classId() {
		return risk_classId;
	}
	public void setRisk_classId(int risk_classId) {
		this.risk_classId = risk_classId;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

	
	
	

}
